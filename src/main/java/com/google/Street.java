package com.google;

public class Street {

    int id;
    String name;
    int start;
    int end;
    int time;

    public Street(int id, String name, int start, int end, int time) {
        this.id = id;
        this.name = name;
        this.start = start;
        this.end = end;
        this.time = time;
    }
}
